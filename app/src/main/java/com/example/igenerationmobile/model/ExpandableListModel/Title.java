package com.example.igenerationmobile.model.ExpandableListModel;

public class Title {
    private String title;
    private int id;

    public Title(String title, int id) {
        this.title = title;
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public int getId() {
        return id;
    }
}
